
#!/bin/bash
# Create certificates for all Kubernetes parts

echo Create certificates for all Kubernetes parts
echo
echo -------------------------------------------------------------------------------------------------
echo
echo Part 1 - Certificate Authority

cat > ca-config.json <<EOF
{
  "signing": {
    "default": {
      "expiry": "8760h"
    },
    "profiles": {
      "kubernetes": {
        "usages": ["signing", "key encipherment", "server auth", "client auth"],
        "expiry": "8760h"
      }
    }
  }
}
EOF

cat > ca-csr.json <<EOF
{
  "CN": "Kubernetes",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "Kubernetes",
      "OU": "CA",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert -initca ca-csr.json | cfssljson -bare ca

echo
echo -------------------------------------------------------------------------------------------------
echo
echo End of Part 1
echo
echo -------------------------------------------------------------------------------------------------
echo
echo Part 2 - The Admin Client Certificate
echo
echo -------------------------------------------------------------------------------------------------
echo

cat > admin-csr.json <<EOF
{
  "CN": "admin",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:masters",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

cfssl gencert \
  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -profile=kubernetes \
  admin-csr.json | cfssljson -bare admin

echo
echo -------------------------------------------------------------------------------------------------
echo
echo End of part 2
echo
echo -------------------------------------------------------------------------------------------------
echo
echo Part 3 - The Kubelet Client Certificates
echo
echo -------------------------------------------------------------------------------------------------
echo

for instance in worker-0 worker-1 worker-2; do
cat > ${instance}-csr.json <<EOF
{
  "CN": "system:node:${instance}",
  "key": {
    "algo": "rsa",
    "size": 2048
  },
  "names": [
    {
      "C": "US",
      "L": "Portland",
      "O": "system:nodes",
      "OU": "Kubernetes The Hard Way",
      "ST": "Oregon"
    }
  ]
}
EOF

EXTERNAL_IP=$(aws ec2 describe-instances \
    --filters "Name=tag:Name,Values=$instance" \
    --query 'Reservations[*].Instances[*].[PublicIpAddress]' \
    --output text)
echo $EXTERNAL_IP

INTERNAL_IP=$(aws ec2 describe-instances \
    --filters "Name=tag:Name,Values=$instance" \
    --query 'Reservations[*].Instances[*].[PrivateIpAddress]' \
    --output text)
echo $INTERNAL_IP

cfssl gencert \
  -ca=ca.pem \
  -ca-key=ca-key.pem \
  -config=ca-config.json \
  -hostname=${instance},${EXTERNAL_IP},${INTERNAL_IP} \
  -profile=kubernetes \
  ${instance}-csr.json | cfssljson -bare ${instance}

done

echo
echo -------------------------------------------------------------------------------------------------
echo
echo End of part 3
echo
echo -------------------------------------------------------------------------------------------------
echo
echo Part 4 - The Controller Manager Client Certificate
echo
echo -------------------------------------------------------------------------------------------------
echo

